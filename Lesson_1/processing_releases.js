function processReleaseData(data) {
  /*
  Iterate through objects
  filter objects that have id and title present
  transform objects to only display id and title data

  Generate an array of objects
    - contains id, title
    - keep releases that have both id and title properties
    - keep only id and title data
  */

  return data.filter(movie => {
    return !!movie.title && !!movie.id
  }).map(movie => {
    return {
      id: movie.id,
      title: movie.title,
    }
  });
}


